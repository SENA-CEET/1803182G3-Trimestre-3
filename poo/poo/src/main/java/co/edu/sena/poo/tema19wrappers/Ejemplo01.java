package co.edu.sena.poo.tema19wrappers;

public class Ejemplo01 {
    public static void main(String[] args) {
        Byte a = 1; // wrappers de byte
        byte a1 = 1;
        System.out.println(Byte.MIN_VALUE);
        System.out.println(Byte.MAX_VALUE);

        Short b= 10; // wrapper de short
        short b1 = 10;
        System.out.println("longitud minima "+Short.MIN_VALUE);
        System.out.println("longitud maxima "+Short.MAX_VALUE);
        System.out.println(Short.BYTES+ " bytes");
        System.out.println(Short.SIZE+ " bits");

        Integer c = 10; // wrapper de int
        int c1 = 10;


        Long d = 10L; // wrapper de long
        long d1 = 10L;

        Float e = 13.5F; // wrapper de float
        float e1 = 34.7F;

        Double f = 3434.23; // wrapper de double
        double f1 = 3434.23;

        Character g = 'A'; // wrapper de char
        char g1 = 'A';


        Boolean h = true; // wrapper de boolean
        boolean h1 = true;












    }
}
